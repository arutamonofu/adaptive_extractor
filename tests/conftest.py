@pytest.fixture
def cleanup_temp_files():
    """Fixture to clean up temporary files after tests.

    Yields:
        Cleanup function.
    """
    temp_files = []

    def register_cleanup(path: Path):
        temp_files.append(path)
        return path

    yield register_cleanup

    # Cleanup
    for path in temp_files:
        if path.exists():
            if path.is_file():
                path.unlink()
            elif path.is_dir():
                shutil.rmtree(path)


