@pytest.fixture(scope="module")
def nanozyme_task(tmp_nanozymes_task_yaml: Path, nanozyme_test_instruction_path: Path):
    """Get nanozyme task components.

    Uses module scope to avoid reloading task for every test.
    Uses temporary task YAML file generated by fixture to avoid dependency
    on external config files.

    Args:
        tmp_nanozymes_task_yaml: Path to temporary nanozymes.yaml file.
        nanozyme_test_instruction_path: Path to test instruction file.

    Returns:
        Dictionary with task components (config, experiment_model, output_model, row_converter).
        Note: signature is created lazily on first access as it requires instruction file.
    """
    from aee.domain.tasks import (
        create_all_models,
        create_row_converter,
        create_signature,
        load_task_from_yaml,
    )

    # Load task from temporary YAML (without instruction file initially)
    config = load_task_from_yaml(tmp_nanozymes_task_yaml)
